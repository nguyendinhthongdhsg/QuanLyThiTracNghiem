    private void display() {
        jTextFieldUserName.setText(oldUser.getUserName());
        jTextFieldUserFullName.setText(oldUser.getUserFullName());
        jTextFieldEmail.setText(oldUser.getUserEmail());
        jPasswordFieldPassword.setText(oldUser.getUserPassword());
        jPasswordFieldRePassword.setText(oldUser.getUserPassword());
        jComboBoxDecentralization.setSelectedIndex(oldUser.getIsAdmin() == 1 ? 1 : 0);
    }
    
    private void jButtonUpdateMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonUpdateMouseReleased
        String userName = (String) jTextFieldUserName.getText();
        String userFullName = (String) jTextFieldUserFullName.getText();
        String email = (String) jTextFieldEmail.getText();
        String password = (String) jPasswordFieldPassword.getText();
        String rePassword = (String) jPasswordFieldRePassword.getText();
        String decentralization = (String) jComboBoxDecentralization.getSelectedItem();

        if(userName.isBlank() && userFullName.isBlank() && email.isBlank()
            && password.isBlank() && rePassword.isBlank() && decentralization.isBlank()) {
            JOptionPane.showMessageDialog(
                this,
                "Vui lòng nhập đầy đủ thông tin!",
                "Cảnh Báo",
                JOptionPane.WARNING_MESSAGE
            );
        } else {
            if(!password.equals(rePassword)) {
                JOptionPane.showMessageDialog(
                    this,
                    "Mật khẩu nhập lại không khớp!",
                    "Cảnh Báo",
                    JOptionPane.WARNING_MESSAGE
                );
            } else {
                UsersDTO newUser = new UsersDTO();
                newUser.setUserID(oldUser.getUserID());
                newUser.setUserName(userName);
                newUser.setUserFullName(userFullName);
                newUser.setUserEmail(email);
                newUser.setUserPassword(password);
                newUser.setIsAdmin((byte)(decentralization.equals("Người dùng") ? 0 : 1));
                UsersBLL userBLL = new UsersBLL();
                String result = userBLL.update(newUser);
                JOptionPane.showMessageDialog(this, result, "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButtonUpdateMouseReleased

